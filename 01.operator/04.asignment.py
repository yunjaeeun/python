# 복합 연산자
print(2 + 3 * 4)   # 14
print((2 + 3) * 4) # 20

number = 2 + 3 * 4
print(number)           # 14

number = number + 2     
print(number)           # 16

number += 2 
print(number)           # 18

number *= 2 
print(number)           # 36

number /= 3 
print(number)           # 12.0

number -= 2 
print(number)           # 10.0

number %= 3 
print(number)           # 1.0